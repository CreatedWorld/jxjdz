package avatar.module.express;

import java.util.List;

public class Rule {
    private String express;

    private double score;

    private String name;

    private List<String> excludeHuActions;

    public List<String> getExcludeHuActions() {
        return excludeHuActions;
    }

    public void setExcludeHuActions(List<String> excludeHuActions) {
        this.excludeHuActions = excludeHuActions;
    }

    public String getExpress() {
        return express;
    }

    public void setExpress(String express) {
        this.express = express;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Rule{" +
                "express='" + express + '\'' +
                ", score=" + score +
                ", name='" + name + '\'' +
                '}';
    }
}
