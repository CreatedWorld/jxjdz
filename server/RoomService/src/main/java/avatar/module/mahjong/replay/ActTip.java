package avatar.module.mahjong.replay;

import java.util.Map;

public class ActTip {
    private String optUserId;

    private String tipRemainTime;

    private String tipRemainUT;

    private Map<String, String> acts;

    private Map<String, String> actCards;

    public String getOptUserId() {
        return optUserId;
    }

    public void setOptUserId(String optUserId) {
        this.optUserId = optUserId;
    }

    public String getTipRemainTime() {
        return tipRemainTime;
    }

    public void setTipRemainTime(String tipRemainTime) {
        this.tipRemainTime = tipRemainTime;
    }

    public String getTipRemainUT() {
        return tipRemainUT;
    }

    public void setTipRemainUT(String tipRemainUT) {
        this.tipRemainUT = tipRemainUT;
    }

    public Map<String, String> getActs() {
        return acts;
    }

    public void setActs(Map<String, String> acts) {
        this.acts = acts;
    }

    public Map<String, String> getActCards() {
        return actCards;
    }

    public void setActCards(Map<String, String> actCards) {
        this.actCards = actCards;
    }
}
