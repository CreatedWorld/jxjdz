using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using UnityEngine.UI;
/// <summary>
/// 创建房间,游戏规则复选框
/// </summary>
public class GameRuleInfo
{
    public GameRuleInfo(Toggle toggle, GameRule rule)
    {
        Toggle = toggle;
        Rule = rule;
    }
    /// <summary>
    /// 游戏规则
    /// </summary>
    public GameRule Rule { get; private set; }
    /// <summary>
    /// 选项控件
    /// </summary>
    public Toggle Toggle { get; private set; }
}